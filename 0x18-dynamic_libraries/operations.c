#include "operations.h"

/**
 * add -> Adds two integers:)
 * @a: first integer.
 * @b: second integer.
 * Return: sum of a & b:)
*/

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub -> subtracts two integers:)
 * @a: first integer:)
 * @b: second integer:)
 * Return: difference of a & b:)
*/

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul -> multiplie two integers:)
 * @a: first integer:)
 * @b: second integer:)
 * Return: product of a and b:)
*/

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div -> divides two integers:)
 * @a: first integer:)
 * @b: second integer:)
 * Return: quotient of a and b:)
*/

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod -> modulus of two integers:)
 * @a: first integer:)
 * @b: second integer:)
 * Return: Remainder of a and b:)
*/

int mod(int a, int b)
{
	return (a % b);
}
